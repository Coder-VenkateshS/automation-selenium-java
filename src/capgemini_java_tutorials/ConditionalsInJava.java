package capgemini_java_tutorials;

public class ConditionalsInJava {

	public static void main(String[] args) {
		// if 
		// if else
		// if elseif ladder
		
		int a = 30;
		int b = 20;
		int c = 25;
		
		if(a < b)
			System.out.println(a);
			else if (b < c)
				System.out.println(b);
			else 
				System.out.println(c);

	}

}
